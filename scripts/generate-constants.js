const fs = require('fs');
const path = require('path');

function getAbi(filePath, contractName) {
  const fullPath = path.join(__dirname, '..', filePath, `${contractName}.json`);
  if (!fs.existsSync(fullPath)) {
    console.error(`Artifact not found: ${fullPath}`);
    process.exit(1);
  }
  const artifact = JSON.parse(fs.readFileSync(fullPath, 'utf8'));
  return JSON.stringify(artifact.abi, null, 2);
}

function getDeployedAddresses(network) {
  const deploymentFile = path.join(__dirname, '..', 'deployments', `${network}.json`);
  if (fs.existsSync(deploymentFile)) {
    const deployment = JSON.parse(fs.readFileSync(deploymentFile, 'utf8'));
    console.log(`Reading deployed addresses from: ${deploymentFile}`);
    return {
      nft: deployment.contracts.CyberNFT.address,
      marketplace: deployment.contracts.CyberNFTMarketplace.address,
      auction: deployment.contracts.CyberNFTAuction.address,
      explorerBase: deployment.explorerBaseUrl,
      chainId: deployment.chainId,
    };
  }
  return null;
}

function main() {
  const network = process.argv[2] || 'amoy';
  const nftAbi = getAbi('artifacts/contracts/NFT.sol', 'CyberNFT');
  const marketplaceAbi = getAbi('artifacts/contracts/NFTMarketplace.sol', 'CyberNFTMarketplace');
  const auctionAbi = getAbi('artifacts/contracts/NFTAuction.sol', 'CyberNFTAuction');

  const deployed = getDeployedAddresses(network);

  const nftFallback = deployed?.nft || "0x5FbDB2315678afecb367f032d93F642f64180aa3";
  const marketplaceFallback = deployed?.marketplace || "0xe7f1725E7734CE288F8367e1Bb143E90bb3F0512";
  const auctionFallback = deployed?.auction || "0x9fE46736679d2D9a65F0992F2272dE9f3c7fa6e0";
  const explorerBase = deployed?.explorerBase || "https://amoy.polygonscan.com";
  const chainId = deployed?.chainId || 80002;

  const content = `// This file is auto-generated by scripts/generate-constants.js. Do not edit manually.

export const NFT_ADDRESS = (process.env.NEXT_PUBLIC_NFT_ADDRESS || "${nftFallback}") as \`0x\${string}\`;
export const MARKETPLACE_ADDRESS = (process.env.NEXT_PUBLIC_MARKETPLACE_ADDRESS || "${marketplaceFallback}") as \`0x\${string}\`;
export const AUCTION_ADDRESS = (process.env.NEXT_PUBLIC_AUCTION_ADDRESS || "${auctionFallback}") as \`0x\${string}\`;

export const CHAIN_ID = ${chainId};
export const EXPLORER_BASE_URL = "${explorerBase}";

/** Build a Polygonscan/Etherscan URL for a transaction hash */
export function getExplorerTxUrl(hash: string): string {
  return \`\${EXPLORER_BASE_URL}/tx/\${hash}\`;
}

/** Build a Polygonscan/Etherscan URL for a contract or wallet address */
export function getExplorerAddressUrl(address: string): string {
  return \`\${EXPLORER_BASE_URL}/address/\${address}\`;
}

export const NFT_ABI = ${nftAbi} as const;

export const MARKETPLACE_ABI = ${marketplaceAbi} as const;

export const AUCTION_ABI = ${auctionAbi} as const;
`;

  const outputPath = path.join(__dirname, '..', 'src', 'utils', 'constants.ts');
  fs.writeFileSync(outputPath, content, 'utf8');
  console.log(`Successfully generated constants at: ${outputPath}`);

  if (deployed) {
    console.log(`Using deployed addresses from deployments/${network}.json`);
    console.log(`  NFT:         ${nftFallback}`);
    console.log(`  Marketplace: ${marketplaceFallback}`);
    console.log(`  Auction:     ${auctionFallback}`);
  } else {
    console.log(`No deployment file found for '${network}'. Using hardhat default addresses.`);
    console.log(`Deploy first with: npx hardhat run scripts/deploy.js --network ${network}`);
  }
}

main();
